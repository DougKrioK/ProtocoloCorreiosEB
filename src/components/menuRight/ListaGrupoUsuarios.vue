<template>
  <q-layout-drawer v-model="mostraMenuRight" side="right">
    <q-list no-border link inset-delimiter>
      <q-list-header>Grupos de Usu√°rios</q-list-header>

        <q-search
          @input="pesquisando"
          v-model.lazy="busca"
          :debounce="600"
          placeholder="Busca por Grupo"
          icon="search"
          float-label="Pesquisa"
        />

        <q-infinite-scroll :handler="loadMore" ref="infiniteScroll" >
          <!-- Content, in this case some <p> tags -->
          <q-item class="item-lista" multiline v-for="(grupoUsuario, index) in listaDeRegistros" :class="index%2 ? 'bg-blue-grey-1' : 'bg-blue-grey-2'" item :to="{ name: 'alterarGrupoUsuario', params: { id: grupoUsuario.grupoUsuario} }" exact :key="index">
            <q-item-main>
              <q-item-tile label> {{ grupoUsuario.codigo }}</q-item-tile>
              <q-item-tile sublabel v-if="grupoUsuario.descricao">{{ grupoUsuario.descricao }}</q-item-tile>
            </q-item-main>
          </q-item>
          <div slot="message" class="row justify-center" style="margin-bottom: 50px;">
            <q-spinner-dots :size="40" />
          </div>
        </q-infinite-scroll>

    </q-list>
  </q-layout-drawer>
</template>

<script>
import { mapGetters } from 'vuex'
import GrupoUsuario from 'src/services/grupoUsuario/GrupoUsuario'
import grupoUsuarioService from 'src/services/grupoUsuario/GrupoUsuarioService'
export default {
  name: 'ListaGrupoDeUsuarios',
  components: {
  },
  data () {
    return {
      mascara: '',
      masked: false,
      busca: '',
      registros: [],
      listaDeRegistros: [],
      inicio: 0,
      fim: 200,
      mostraMenuRight: true
    }
  },
  methods: {
    loadMore (index, done) {
      console.log('vou atualizar a lista em grupoUsuarios')
      let inicio = this.inicio
      let fim = this.fim
      let busca = this.busca
      grupoUsuarioService.procura(busca, inicio, fim)
        .then(result => {
          if (result.data.registros.length === 0 && inicio === 0) {
            console.log('sem registros')
            this.listaDeRegistros = [{
              label: 'Sem registros encontrados'
            }]
            this.registros = []
            this.$refs.infiniteScroll.stop()
          } else {
            if (inicio === 0) {
              this.registros = []
            }
            this.registros = this.registros.concat(result.data.registros)
            this.listaDeRegistros = this.registros
            // this.$store.commit('menuRight/setRegistros', listaDeRegistros)
            if (result.data.fim === true) {
              console.log('fim da lista')
              this.$refs.infiniteScroll.stop()
            }
            this.inicio = fim
            this.fim = fim + 200
          }
          done()
        })
    },
    pesquisando () {
      console.log('pesquisando...')
      this.registros = []
      this.listaDeRegistros = []
      this.inicio = 0
      this.fim = 200
      this.$refs.infiniteScroll.resume()
    }
  },
  computed: {
    ...mapGetters({
      // getRegistros: 'menuRight/getRegistros'
    })
  },
  mounted () {
    this.$root.$on('toggleRight', () => {
      this.mostraMenuRight = !this.mostraMenuRight
    })

    this.$root.$on('removeRegistro', (grupoUsuario) => {
      let idRegistro = this.registros.filter(registro => registro.grupoUsuario === grupoUsuario)
      this.registros.splice(this.registros.indexOf(idRegistro[0]), 1)
      this.listaDeRegistros = this.registros
    })

    this.$root.$on('alteraUnicoRegistro', (novoRegistro) => {
      let idRegistro = this.registros.filter(registro => registro.grupoUsuario === novoRegistro.grupoUsuario)
      let id = this.registros.indexOf(idRegistro[0])
      this.registros[id].codigo = novoRegistro.codigo
      this.registros[id].descricao = novoRegistro.descricao
      this.listaDeRegistros = this.registros
    })

    this.$root.$on('adicionaRegistroNaLista', (obj) => {
      let grupoUsuario = new GrupoUsuario(obj.grupoUsuario, obj.codigo, obj.descricao)
      this.registros.push(grupoUsuario)
      this.listaDeRegistros = this.registros
    })
  },
  beforeDestroy () {
    this.$root.$off('toggleRight')
    this.$root.$off('removeRegistro')
    this.$root.$off('alteraUnicoRegistro')
    this.$root.$off('adicionaRegistroNaLista')
  }
}
</script>
